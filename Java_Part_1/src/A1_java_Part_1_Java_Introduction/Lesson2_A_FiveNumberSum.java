package A1_java_Part_1_Java_Introduction;
/*
 * Add five integers and display their sum
 */
public class Lesson2_A_FiveNumberSum {

	public static void main(String[] args) { //declare 5 integer variable and assign values
		int number1=11;
		int number2=22;
		int number3=33;
		int number4=44;
		int number5=55;
		int sum; //declare integer variable (sum)to hold sum of numbers.
		sum =number1+number2+number3+number4+number5;
		System.out.print("the sum is "); //print text before (sum) value.
		System.out.println(sum); //Print sum value.
	}

}
